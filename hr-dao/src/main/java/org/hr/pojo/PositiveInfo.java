package org.hr.pojo;

public class PositiveInfo {
    private Integer id;

    private Integer contracperiod;

    private String positivewage;

    private String positiveoperant;

    private String positiveexpire;

    private Integer contractinfoId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getContracperiod() {
        return contracperiod;
    }

    public void setContracperiod(Integer contracperiod) {
        this.contracperiod = contracperiod;
    }

    public String getPositivewage() {
        return positivewage;
    }

    public void setPositivewage(String positivewage) {
        this.positivewage = positivewage;
    }

    public String getPositiveoperant() {
        return positiveoperant;
    }

    public void setPositiveoperant(String positiveoperant) {
        this.positiveoperant = positiveoperant;
    }

    public String getPositiveexpire() {
        return positiveexpire;
    }

    public void setPositiveexpire(String positiveexpire) {
        this.positiveexpire = positiveexpire;
    }

    public Integer getContractinfoId() {
        return contractinfoId;
    }

    public void setContractinfoId(Integer contractinfoId) {
        this.contractinfoId = contractinfoId;
    }
}