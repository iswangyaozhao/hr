package org.hr.pojo;

public class TrialInfo {
    private Integer id;

    private Integer trialperiod;

    private String trialwage;

    private String trialoperant;

    private String trialexpire;

    private Integer contractinfoId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getTrialperiod() {
        return trialperiod;
    }

    public void setTrialperiod(Integer trialperiod) {
        this.trialperiod = trialperiod;
    }

    public String getTrialwage() {
        return trialwage;
    }

    public void setTrialwage(String trialwage) {
        this.trialwage = trialwage;
    }

    public String getTrialoperant() {
        return trialoperant;
    }

    public void setTrialoperant(String trialoperant) {
        this.trialoperant = trialoperant;
    }

    public String getTrialexpire() {
        return trialexpire;
    }

    public void setTrialexpire(String trialexpire) {
        this.trialexpire = trialexpire;
    }

    public Integer getContractinfoId() {
        return contractinfoId;
    }

    public void setContractinfoId(Integer contractinfoId) {
        this.contractinfoId = contractinfoId;
    }
}