package org.hr.pojo;

public class ContractInfo {
    private Integer id;

    private String contractno;

    private String name;

    private Integer type;

    private Integer deadline;

    private Integer positive;

    private String signing;

    private String contractyear;

    private Integer contracttextId;

    private Integer trialinfoId;

    private Integer positiveinfoId;

    private Integer contractstate;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getContractno() {
        return contractno;
    }

    public void setContractno(String contractno) {
        this.contractno = contractno;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public Integer getDeadline() {
        return deadline;
    }

    public void setDeadline(Integer deadline) {
        this.deadline = deadline;
    }

    public Integer getPositive() {
        return positive;
    }

    public void setPositive(Integer positive) {
        this.positive = positive;
    }

    public String getSigning() {
        return signing;
    }

    public void setSigning(String signing) {
        this.signing = signing;
    }

    public String getContractyear() {
        return contractyear;
    }

    public void setContractyear(String contractyear) {
        this.contractyear = contractyear;
    }

    public Integer getContracttextId() {
        return contracttextId;
    }

    public void setContracttextId(Integer contracttextId) {
        this.contracttextId = contracttextId;
    }

    public Integer getTrialinfoId() {
        return trialinfoId;
    }

    public void setTrialinfoId(Integer trialinfoId) {
        this.trialinfoId = trialinfoId;
    }

    public Integer getPositiveinfoId() {
        return positiveinfoId;
    }

    public void setPositiveinfoId(Integer positiveinfoId) {
        this.positiveinfoId = positiveinfoId;
    }

    public Integer getContractstate() {
        return contractstate;
    }

    public void setContractstate(Integer contractstate) {
        this.contractstate = contractstate;
    }
}